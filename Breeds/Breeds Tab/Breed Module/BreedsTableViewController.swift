        BreedTransferObject(name: "basenji", subBreed: nil, image: "image1", favourites: true),
        BreedTransferObject(name: "australian", subBreed: [SubBreedTransferObject(name: "shepherd", image: "image2", favourites: true)], image: "image4", favourites: false),
        BreedTransferObject(name: "airedale", subBreed: nil, image: "image3", favourites: false)]
    
